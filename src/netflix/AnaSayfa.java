    private void jButton63ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton63ActionPerformed
        String program="Recep İvedik";
        String tur="Aksiyon ve Macera";
        KayitSayfasi ks = new KayitSayfasi();
        String puan = j1.getText();
        String query = "INSERT INTO 'kullaniciprogram' ('puan','program_adi') VALUES (?,?)";
        String query2 = "INSERT INTO 'programtur' ('program_adi','tur_adi','puan') VALUES (?,?,?)";
        PreparedStatement ps = null;
        PreparedStatement ps2 = null;
        try{
            ps= Baglanti.Bagla().prepareStatement(query);
            ps.setString(1,puan);
            ps.setString(2,program);
            ps2=Baglanti.Bagla().prepareStatement(query2);
            ps2.setString(1,program);
            ps2.setString(2,tur);
            ps2.setString(3,puan);
            if(ps.executeUpdate() > 0 && ps2.executeUpdate() > 0)
            {
                AnaSayfa as=new AnaSayfa();
                as.setVisible(true);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex);
        }
        finally{
            try{
                ps.close();
                ps2.close();
            }catch(Exception ex){
                
            }
        }
    }//GEN-LAST:event_jButton63ActionPerformed

